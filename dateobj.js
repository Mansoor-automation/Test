date.getFullYear();; // returns the year (four digits)
date.getHours();; // returns the hour (from 0 to 23)
date.getMilliseconds();; // returns the milliseconds (from 0 to 999)
date.getMinutes();; // returns the minutes (from 0 to 59)
date.getMonth();;; // returns the month (from 0 to 11)
date.getSeconds();; // returns the seconds (from 0 to 59)
date.getTime();; // returns the number of milliseconds since January 1, 1970
date.getTimezoneOffset();; // returns the time difference between UTC time and local time, in minutes
date.getUTCDate();; // returns the day of the month, according to universal time (from 1 to 31)
date.getUTCDay();;  // returns the day of the week, according to universal time (from 0 to 6)
